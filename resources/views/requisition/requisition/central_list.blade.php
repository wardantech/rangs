            function deleteItem(id) {
                $.ajax({
                    type: "GET",
                    url:"job/delete/"+id,

                    success: function (resp) {

                        console.log(resp);

                        // Reloade DataTable
                        $('#datatable').DataTable().ajax.reload();

                        if (resp.success === true) {
                            // show toast message
